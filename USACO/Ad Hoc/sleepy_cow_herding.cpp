#include <bits/stdc++.h>

int main() {
    freopen("herding.in", "r", stdin);
    freopen("herding.out", "w", stdout);
    
    int a, b, c;
    std::cin >> a >> b >> c;
    
    // Сортируем позиции коров
    std::vector<int> v{a, b, c};
    std::sort(v.begin(), v.end());
    
    int diff1 = v[1] - v[0];  // Расстояние между первой и второй коровой
    int diff2 = v[2] - v[1];  // Расстояние между второй и третьей коровой
    
    // Вычисляем минимальное количество перемещений
    if (diff1 == 1 && diff2 == 1) {
        // Все коровы уже на соседних позициях
        std::cout << 0 << '\n';
    } else if (diff1 == 2 || diff2 == 2) {
        // Если хотя бы одна пара коров имеет расстояние 2, требуется одно перемещение
        std::cout << 1 << '\n';
    } else {
        // Во всех других случаях потребуется два перемещения
        std::cout << 2 << '\n';
    }
    
    // Максимальное количество перемещений — это самое большое расстояние между соседями минус 1
    std::cout << std::max(diff1, diff2) - 1 << '\n';
    
    return 0;
}
